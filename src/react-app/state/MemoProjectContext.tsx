interface State {
  // The company the user picked from the search box BEFORE uploading. This
  // is the authoritative project identity — it overrides the heuristic
  // company/ticker that period detection guesses from the memo body (which
  // can latch onto a segment line-item, e.g. "Lloyd Electric" in a Havells
  // report). null until a company is selected; the upload slot stays gated
  // until then.
  selectedCompany: SelectedCompany | null;
  initialFile: LocalUploadedFile | null;
  extraction: ExtractionResult | null;
  extractionStatus: ExtractionStatus;
  dna: MemoDNA | null;
  detection: PeriodDetectionResult | null;
  // Coverage signals computed deterministically from the extracted text the
  // moment the memo is uploaded. Drives "skip the Shareholding section if
  // the memo never mentioned shareholding" — the user's explicit ask: the
  // follow-up memo should only contain what the original memo covered.
  memoCoverage: MemoCoverageSignals | null;
  periodOverride: PeriodOverride;
  research: ResearchFindings | null;
  researchState: ResearchGenerationState;
  researchProgress: ResearchProgress;
  // Stage 1 of the research rearchitecture: the comprehensive, company-wide
  // research report (kept internally, condensed into the memo in Stage 2, and
  // used to answer follow-up Q&A in Stage 3). Additive for now — it does not
  // yet feed memo generation.
  fullReport: FullResearchReportState;
  fullReportProgress: ResearchReportSectionRunState[];
  generatedMemo: FollowUpMemo | null;
  llm: LlmGenerationState;
  // Dashboard-only priorities Q&A — never enters the downloadable memo.
  // Generated by /api/generate/priorities-answer right after the memo
  // succeeds, IF the user typed anything into the priorities textarea.
  prioritiesAnswer: PrioritiesAnswerState;
  progress: MemoGenerationProgress;
  completedSections: Partial<Record<CanonicalSectionId, MemoSection>>;
  llmProviderStatus: LlmStatusResponse | null;
  demoFollowUpMemo: FollowUpMemo | null;
  gateTokenSet: boolean;
  // Phase 6A: Memo Understanding Engine state.
  understanding: MemoUnderstandingState;
  // Emergency / developer-only escape: when true, the Research button
  // skips the requirement that memoUnderstanding succeed first. Surfaced
  // only inside a <details> disclosure in MemoUnderstandingCard.
  skipUnderstanding: boolean;
  // Phase 6C: free-text the user typed into the "What else should we
  // test?" priorities textbox. Threaded into every research pass and
  // every memo section so the memo addresses what the user asked.
  userResearchPriorities: string;
  // Phase 6H: true when this browser bundle's build id no longer matches
  // the deployed worker's (a stale tab). Drives a reload banner so the
  // user never hits cryptic "stale_client" 400s mid-generation.
  staleClient: boolean;
}

