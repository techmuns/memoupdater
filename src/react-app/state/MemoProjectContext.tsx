function buildInitialProgress(): MemoGenerationProgress {
  return {
    kind: "idle",
    sections: CANONICAL_SECTION_IDS.map<SectionRunState>((id) => ({
      id,
      title: SECTION_TITLES[id],
      status: "pending",
      attempt: 0,
    })),
    completedCount: 0,
  };
}

function buildInitialResearchProgress(): ResearchProgress {
  return {
    kind: "idle",
    passes: RESEARCH_PASS_IDS.map<ResearchPassRunState>((id) => ({
      id,
      title: RESEARCH_PASS_TITLES[id],
      status: "pending",
      attempt: 0,
    })),
    failedPassIds: [],
  };
}

function buildInitialReportProgress(): ResearchReportSectionRunState[] {
  return RESEARCH_REPORT_SECTION_ORDER.map((id) => ({
    id,
    title: RESEARCH_REPORT_SECTION_TITLES[id],
    status: "pending",
    attempt: 0,
  }));
}

