function reducer(state: State, action: Action): State {
  switch (action.type) {
    case "SET_SELECTED_COMPANY":
      return {
        ...state,
        selectedCompany: action.company,
        // Mirror the picked name into the period override so the header chip
        // and every downstream company-name lookup reflect it immediately,
        // even before a memo is uploaded.
        periodOverride: {
          ...state.periodOverride,
          detectedCompany: action.company.companyName,
        },
      };
    case "CLEAR_SELECTED_COMPANY":
      return { ...state, selectedCompany: null };
    case "SET_INITIAL_FILE":
      return { ...state, initialFile: action.file };
    case "SET_EXTRACTION_STATUS":
      return { ...state, extractionStatus: action.status };
    case "SET_EXTRACTION":
      return {
        ...state,
        extraction: action.result,
        extractionStatus: action.result.status,
      };
    case "SET_DNA_AND_DETECTION":
      return {
        ...state,
        dna: action.dna,
        detection: action.detection,
        memoCoverage: action.coverage,
        periodOverride: {
          // The user-picked company wins over the heuristic guess; period
          // detection (start month, label) is still trusted and applied.
          detectedCompany:
            state.selectedCompany?.companyName ??
            action.detection.detectedCompany,
          periodLabel: action.detection.best
            ? renderPeriodLabel(action.detection.best)
            : undefined,
          researchStart: action.detection.researchStart,
        },
        research: null,
        researchState: { kind: "idle" },
        researchProgress: buildInitialResearchProgress(),
        fullReport: { kind: "idle" },
        fullReportProgress: buildInitialReportProgress(),
        generatedMemo: null,
        llm: { kind: "idle" },
        progress: buildInitialProgress(),
        completedSections: {},
        // Phase 6A: a new memo invalidates any prior understanding +
        // resets the escape-hatch flag.
        understanding: { kind: "idle" },
        skipUnderstanding: false,
      };
    case "SET_PERIOD_OVERRIDE":
      return {
        ...state,
        periodOverride: { ...state.periodOverride, ...action.override },
      };
    case "SET_RESEARCH_STATE":
      return { ...state, researchState: action.state };
    case "SET_RESEARCH":
      return { ...state, research: action.research };
    case "START_RESEARCH_RUN": {
      const fresh = buildInitialResearchProgress();
      const passes = fresh.passes.map<ResearchPassRunState>((row) => {
        const prev = state.researchProgress.passes.find((p) => p.id === row.id);
        if (
          action.preserveExistingSuccesses &&
          prev &&
          prev.status === "success" &&
          !action.runPassIds.includes(row.id)
        ) {
          return { ...prev, attempt: 0, errorCode: undefined, errorMessage: undefined };
        }
        return row;
      });
      return {
        ...state,
        researchProgress: {
          kind: "running",
          startedAt: action.startedAt,
          passes,
          failedPassIds: [],
        },
      };
    }
    case "RESEARCH_PASS_STARTED": {
      const passes = state.researchProgress.passes.map<ResearchPassRunState>(
        (row) =>
          row.id === action.passId
            ? {
                ...row,
                status: "running",
                attempt: action.attempt,
                errorCode: undefined,
                errorMessage: undefined,
              }
            : row,
      );
      return {
        ...state,
        researchProgress: { ...state.researchProgress, passes },
      };
    }
    case "RESEARCH_PASS_SUCCESS": {
      const passes = state.researchProgress.passes.map<ResearchPassRunState>(
        (row) =>
          row.id === action.passId
            ? { ...row, status: "success", findingCount: action.findingCount }
            : row,
      );
      return {
        ...state,
        researchProgress: { ...state.researchProgress, passes },
      };
    }
    case "RESEARCH_PASS_FAILED": {
      const passes = state.researchProgress.passes.map<ResearchPassRunState>(
        (row) =>
          row.id === action.passId
            ? {
                ...row,
                status: "failed",
                errorCode: action.code,
                errorMessage: action.message,
              }
            : row,
      );
      const failedPassIds = passes
        .filter((p) => p.status === "failed")
        .map((p) => p.id);
      return {
        ...state,
        researchProgress: {
          ...state.researchProgress,
          passes,
          failedPassIds,
        },
      };
    }
    case "RESEARCH_RUN_TERMINAL":
      return {
        ...state,
        researchProgress: {
          ...state.researchProgress,
          kind: action.kind,
          failedPassIds: action.failedPassIds,
        },
      };
    case "SET_LLM_STATE":
      return { ...state, llm: action.state };
    case "SET_PRIORITIES_ANSWER":
      return { ...state, prioritiesAnswer: action.state };
    case "SET_GENERATED_MEMO":
      return { ...state, generatedMemo: action.memo };
    case "START_GENERATION": {
      const fresh = buildInitialProgress();
      const excluded = new Set(action.excludeSectionIds ?? []);
      const sections = fresh.sections.map<SectionRunState>((row, i) => {
        // Sections the orchestrator won't draft are flagged skipped so the UI
        // hides them instead of showing a permanently-pending row.
        const skipped = excluded.has(row.id);
        const prev =
          i < action.resumeFromIdx ? state.progress.sections[i] : undefined;
        if (prev && prev.status === "success") {
          return { ...prev, attempt: 0, errorCode: undefined, errorMessage: undefined, skipped };
        }
        return { ...row, skipped };
      });
      const completedCount = sections.filter((s) => s.status === "success").length;
      return {
        ...state,
        progress: {
          kind: "running",
          startedAt: action.startedAt,
          sections,
          completedCount,
        },
      };
    }
    case "SECTION_STARTED": {
      const sections = state.progress.sections.map<SectionRunState>((row) =>
        row.id === action.sectionId
          ? {
              ...row,
              status: "running",
              attempt: action.attempt,
              errorCode: undefined,
              errorMessage: undefined,
            }
          : row,
      );
      return { ...state, progress: { ...state.progress, sections } };
    }
    case "SECTION_SUCCESS": {
      const sections = state.progress.sections.map<SectionRunState>((row) =>
        row.id === action.sectionId
          ? { ...row, status: "success" }
          : row,
      );
      const completedCount = sections.filter((s) => s.status === "success").length;
      return {
        ...state,
        completedSections: {
          ...state.completedSections,
          [action.sectionId]: action.section,
        },
        progress: {
          ...state.progress,
          sections,
          completedCount,
        },
      };
    }
    case "SECTION_FAILED": {
      const sections = state.progress.sections.map<SectionRunState>((row) =>
        row.id === action.sectionId
          ? {
              ...row,
              status: "failed",
              errorCode: action.code,
              errorMessage: action.message,
            }
          : row,
      );
      return {
        ...state,
        progress: {
          ...state.progress,
          kind: "failed",
          sections,
          failedSectionId: action.sectionId,
        },
      };
    }
    case "RESET_PROGRESS":
      return {
        ...state,
        progress: buildInitialProgress(),
        completedSections: {},
        generatedMemo: null,
        llm: { kind: "idle" },
        prioritiesAnswer: { kind: "idle" },
      };
    case "SET_LLM_PROVIDER_STATUS":
      return { ...state, llmProviderStatus: action.status };
    case "SET_DEMO_MEMO":
      return { ...state, demoFollowUpMemo: action.memo };
    case "SET_GATE_TOKEN_SET":
      return { ...state, gateTokenSet: action.value };
    case "START_UNDERSTAND":
      return { ...state, understanding: { kind: "loading" } };
    case "SET_UNDERSTANDING": {
      // Memo Intelligence is now the AUTHORITATIVE source for memo metadata
      // — it reads the whole document via the LLM, vs. the cheap regex pass
      // that only scans the head. When it gives us a publishedDate, parse
      // it into ISO and overwrite detection.memoWrittenOn so the two UI
      // surfaces can never contradict each other (the user's specific
      // complaint: "No memo date was extracted" vs the Intelligence card
      // showing "DATED 2024-05-07").
      const llmPublishedDate = action.understanding.memo?.publishedDate;
      const llmIso = parseFreeFormDateToIso(llmPublishedDate);
      const detection = state.detection;
      const nextDetection: PeriodDetectionResult | null =
        detection && llmIso
          ? {
              ...detection,
              memoWrittenOn: llmIso,
              memoWrittenOnRaw: llmPublishedDate ?? detection.memoWrittenOnRaw,
              memoWrittenOnConfidence: "high",
              memoWrittenOnReason:
                "extracted by Memo Intelligence (full-document LLM analysis)",
            }
          : detection;
      return {
        ...state,
        detection: nextDetection,
        understanding: {
          kind: "success",
          understanding: action.understanding,
          providerMetadata: action.providerMetadata,
          warnings: action.warnings ?? [],
        },
        // Successful understanding implicitly clears any prior
        // emergency-skip flag — memo-specific research is the
        // expected path again.
        skipUnderstanding: false,
      };
    }
    case "SET_UNDERSTANDING_ERROR":
      return {
        ...state,
        understanding: {
          kind: "error",
          code: action.code,
          message: action.message,
        },
      };
    case "SET_SKIP_UNDERSTANDING":
      return { ...state, skipUnderstanding: action.value };
    case "SET_USER_RESEARCH_PRIORITIES":
      return { ...state, userResearchPriorities: action.value };
    case "SET_STALE_CLIENT":
      return { ...state, staleClient: action.value };
    case "START_REPORT":
      return {
        ...state,
        fullReport: { kind: "loading" },
        fullReportProgress: buildInitialReportProgress(),
      };
    case "REPORT_SECTION_STARTED":
      return {
        ...state,
        fullReportProgress: state.fullReportProgress.map((row) =>
          row.id === action.id
            ? { ...row, status: "running", attempt: action.attempt, errorCode: undefined }
            : row,
        ),
      };
    case "REPORT_SECTION_SUCCESS":
      return {
        ...state,
        fullReportProgress: state.fullReportProgress.map((row) =>
          row.id === action.section.id
            ? { ...row, status: "success", errorCode: undefined }
            : row,
        ),
      };
    case "REPORT_SECTION_FAILED":
      return {
        ...state,
        fullReportProgress: state.fullReportProgress.map((row) =>
          row.id === action.id
            ? { ...row, status: "failed", errorCode: action.code }
            : row,
        ),
      };
    case "SET_REPORT_STATE":
      return { ...state, fullReport: action.state };
    case "RESET":
      return {
        ...initialState,
        // Start over returns to the company picker — clear the prior pick.
        selectedCompany: null,
        progress: buildInitialProgress(),
        researchProgress: buildInitialResearchProgress(),
        llmProviderStatus: state.llmProviderStatus,
        demoFollowUpMemo: state.demoFollowUpMemo,
        gateTokenSet: state.gateTokenSet,
        understanding: { kind: "idle" },
        skipUnderstanding: false,
        userResearchPriorities: "",
        // A stale browser bundle stays stale across "Start over".
        staleClient: state.staleClient,
      };
  }
}

