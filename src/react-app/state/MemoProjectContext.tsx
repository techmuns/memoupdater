const Ctx = createContext<MemoProjectContextValue | null>(null);

export function MemoProjectProvider({ children }: { children: ReactNode }) {
  const [state, dispatch] = useReducer(reducer, initialState);
  const researchAbort = useRef<AbortController | null>(null);
  const generateAbort = useRef<AbortController | null>(null);
  const understandAbort = useRef<AbortController | null>(null);
  const reportAbort = useRef<AbortController | null>(null);

  useEffect(() => {
    api
      .demoFollowUpMemo()
      .then((memo) => dispatch({ type: "SET_DEMO_MEMO", memo }))
      .catch(() => {});
    api
      .llmStatus()
      .then((status) =>
        dispatch({ type: "SET_LLM_PROVIDER_STATUS", status }),
      )
      .catch(() =>
        dispatch({ type: "SET_LLM_PROVIDER_STATUS", status: null }),
      );
    // Phase 6H: stale-bundle handshake. Compare this bundle's compiled-in
    // build id against the deployed worker's. A mismatch means the tab
    // is running old code against a new worker — surface a reload banner
    // before the user hits a stale_client 400. "dev" (no Vite define,
    // e.g. SSR/tests) never trips it.
    api
      .version()
      .then((v) => {
        if (APP_BUILD_ID !== "dev" && v.buildId !== APP_BUILD_ID) {
          dispatch({ type: "SET_STALE_CLIENT", value: true });
        }
      })
      .catch(() => {});
    dispatch({
      type: "SET_GATE_TOKEN_SET",
      value: Boolean(getLlmGateToken()),
    });
    // Cross-tab gate-token changes:
    const onStorage = (e: StorageEvent): void => {
      if (e.key === GATE_TOKEN_POLL_KEY) {
        dispatch({
          type: "SET_GATE_TOKEN_SET",
          value: Boolean(getLlmGateToken()),
        });
      }
    };
    window.addEventListener("storage", onStorage);
    return () => window.removeEventListener("storage", onStorage);
  }, []);

  const setSelectedCompany = useCallback((company: SelectedCompany): void => {
    dispatch({ type: "SET_SELECTED_COMPANY", company });
  }, []);

  const clearSelectedCompany = useCallback((): void => {
    dispatch({ type: "CLEAR_SELECTED_COMPANY" });
  }, []);

  const extractInitialMemo = useCallback(
    async (file: File): Promise<ExtractionResult> => {
      const ext = getExtension(file.name);
      const local: LocalUploadedFile = {
        id: `local_initial_${Date.now()}`,
        kind: "initial_memo",
        filename: file.name,
        sizeBytes: file.size,
        mime: mimeForFile(file),
        extension: ext,
        uploadedAt: new Date().toISOString(),
        extractionSupported: extractionSupported(ext),
      };
      dispatch({ type: "SET_INITIAL_FILE", file: local });
      dispatch({ type: "SET_EXTRACTION_STATUS", status: "extracting" });
      const result = await extractText(file);
      dispatch({ type: "SET_EXTRACTION", result });
      if (result.status === "success" || result.status === "partial") {
        const dna = buildMemoDnaFromText({
          text: result.text,
          filename: result.source.filename,
        });
        // Period detector finds the memo's TIME ANCHOR (FY26 / Q3 FY25 / …);
        // detectMemoWrittenOn finds the memo's WRITTEN-ON date (the day the
        // author dated it). Both are deterministic regex passes — required
        // before any LLM call so return-period math has a real start date and
        // the period panel can show a confirmable field instead of "fiscal
        // calendar unknown · LOW CONFIDENCE".
        const periodDetection = detectPeriodFromMemoText(result.text);
        const writtenOn = detectMemoWrittenOn(result.text);
        const detection: PeriodDetectionResult = writtenOn
          ? {
              ...periodDetection,
              memoWrittenOn: writtenOn.iso,
              memoWrittenOnRaw: writtenOn.raw,
              memoWrittenOnConfidence: writtenOn.confidence,
              memoWrittenOnReason: writtenOn.reason,
            }
          : periodDetection;
        const coverage = computeMemoCoverage(result.text);
        dispatch({ type: "SET_DNA_AND_DETECTION", dna, detection, coverage });
      }
      return result;
    },
    [],
  );

  const setPeriodOverride = useCallback((override: PeriodOverride) => {
    dispatch({ type: "SET_PERIOD_OVERRIDE", override });
  }, []);

  const refreshLlmProviderStatus = useCallback(async (): Promise<void> => {
    try {
      const status = await api.llmStatus();
      dispatch({ type: "SET_LLM_PROVIDER_STATUS", status });
    } catch {
      dispatch({ type: "SET_LLM_PROVIDER_STATUS", status: null });
    }
  }, []);

  const syncGateTokenSet = useCallback((): void => {
    dispatch({
      type: "SET_GATE_TOKEN_SET",
      value: Boolean(getLlmGateToken()),
    });
  }, []);


  // Phase 6A: Memo Understanding Engine.
  const runMemoUnderstandingOrchestrated = useCallback(async (): Promise<void> => {
    if (!state.dna || !state.extraction || !state.initialFile) return;
    const companyName =
      state.selectedCompany?.companyName ??
      state.periodOverride.detectedCompany ??
      state.detection?.detectedCompany ??
      state.initialFile.filename.replace(/\.[^.]+$/, "");
    const ticker =
      state.selectedCompany?.ticker ?? state.detection?.detectedTicker;
    const detection = state.detection
      ? detectionToResearchDetectionInput(state.detection, companyName)
      : undefined;
    // The picked company is authoritative — don't let the heuristic's
    // detectedCompany leak back in through the detection input.
    if (detection) detection.detectedCompany = companyName;

    understandAbort.current?.abort();
    const controller = new AbortController();
    understandAbort.current = controller;
    dispatch({ type: "START_UNDERSTAND" });

    let response: Awaited<ReturnType<typeof api.memoUnderstand>> | null = null;
    let networkMessage = "";
    try {
      response = await api.memoUnderstand(
        {
          project: {
            id: state.dna.projectId,
            ticker,
            companyName,
            sector: state.selectedCompany?.sector,
          },
          detection,
          memo: {
            id: state.initialFile.id,
            text: state.extraction.text,
            sourceFilename: state.extraction.source.filename,
            sizeBytes: state.extraction.source.sizeBytes,
          },
          dna: state.dna,
        },
        controller.signal,
      );
    } catch (err) {
      networkMessage = err instanceof Error ? err.message : "Network error";
    }
    if (understandAbort.current !== controller) return;
    understandAbort.current = null;

    if (response && response.ok) {
      dispatch({
        type: "SET_UNDERSTANDING",
        understanding: response.understanding,
        providerMetadata: {
          providerName: "openai",
          modelUsed: response.providerMetadata.modelUsed,
        },
        warnings: response.warnings,
      });
      return;
    }
    if (response) {
      dispatch({
        type: "SET_UNDERSTANDING_ERROR",
        code: response.code,
        message: response.message,
      });
      return;
    }
    dispatch({
      type: "SET_UNDERSTANDING_ERROR",
      code: "provider_error",
      message: networkMessage || "Network error",
    });
  }, [
    state.dna,
    state.extraction,
    state.initialFile,
    state.detection,
    state.periodOverride.detectedCompany,
    state.selectedCompany,
  ]);

  const runMemoUnderstanding = useCallback(
    (): Promise<void> => runMemoUnderstandingOrchestrated(),
    [runMemoUnderstandingOrchestrated],
  );
  const rerunMemoUnderstanding = useCallback(
    (): Promise<void> => runMemoUnderstandingOrchestrated(),
    [runMemoUnderstandingOrchestrated],
  );
  const skipMemoUnderstanding = useCallback((): void => {
    dispatch({ type: "SET_SKIP_UNDERSTANDING", value: true });
  }, []);

  const setUserResearchPriorities = useCallback((value: string): void => {
    dispatch({ type: "SET_USER_RESEARCH_PRIORITIES", value });
  }, []);

  // Auto-run memo understanding once DNA + extraction are ready and the
  // provider is configured. Only fires on the idle→loading transition
  // (the dependency check) so subsequent re-renders don't re-trigger.
  const understandKind = state.understanding.kind;
  const llmReadyForUnderstand =
    state.llmProviderStatus?.llmReady === true &&
    state.llmProviderStatus?.researchAvailable === true;
  const understandGateBlocking =
    state.llmProviderStatus?.gateEnabled === true && !state.gateTokenSet;
  useEffect(() => {
    if (understandKind !== "idle") return;
    if (!state.dna || !state.extraction) return;
    if (!llmReadyForUnderstand || understandGateBlocking) return;
    void runMemoUnderstandingOrchestrated();
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [
    understandKind,
    state.dna,
    state.extraction,
    llmReadyForUnderstand,
    understandGateBlocking,
  ]);

  // Stage 1: generate the comprehensive company-wide research report. Runs its
  // own set of web-grounded section calls (independent of the narrowed passes),
  // stores the full report, and is the foundation for the report-driven memo
  // (Stage 2) and Q&A (Stage 3).
  const generateFullResearchReport = useCallback(async (): Promise<void> => {
    if (!state.dna || !state.extraction || !state.initialFile) return;
    const periodLabel =
      state.periodOverride.periodLabel ??
      (state.detection?.best ? renderPeriodLabel(state.detection.best) : "");
    const companyName =
      state.selectedCompany?.companyName ??
      state.periodOverride.detectedCompany ??
      state.detection?.detectedCompany ??
      state.initialFile.filename.replace(/\.[^.]+$/, "");
    const ticker =
      state.selectedCompany?.ticker ?? state.detection?.detectedTicker ?? undefined;

    reportAbort.current?.abort();
    const controller = new AbortController();
    reportAbort.current = controller;

    dispatch({ type: "START_REPORT" });
    // The comprehensive research is now the single research step: it also
    // drives the memo-facing research state (findings assembled from the
    // report sections), so the memo drafter unlocks on success.
    dispatch({ type: "SET_RESEARCH_STATE", state: { kind: "loading" } });

    const memoContext = state.extraction.text?.trim() || undefined;

    const result = await runFullResearchReport({
      project: {
        id: state.dna.projectId,
        companyName,
        ticker,
        sector: state.selectedCompany?.sector,
      },
      companyAliases: {
        longName: companyName,
        aliases: ticker ? [ticker] : undefined,
      },
      detection: {
        periodLabel: periodLabel || "the original memo period",
        researchStart: state.periodOverride.researchStart,
        researchCurrent:
          state.detection?.researchCurrent ??
          new Date().toISOString().slice(0, 7),
        memoWrittenOn: state.detection?.memoWrittenOn,
      },
      memoContext,
      apiCall: (req, signal) => api.researchReportSection(req, signal),
      signal: controller.signal,
      onSectionStart: (id, attempt) =>
        dispatch({ type: "REPORT_SECTION_STARTED", id, attempt }),
      onSectionDone: (section) =>
        dispatch({ type: "REPORT_SECTION_SUCCESS", section }),
      onSectionFail: (id, code) =>
        dispatch({ type: "REPORT_SECTION_FAILED", id, code }),
    });

    if (reportAbort.current !== controller) return;

    if (result.outcome === "aborted") {
      dispatch({ type: "SET_REPORT_STATE", state: { kind: "idle" } });
      dispatch({ type: "SET_RESEARCH_STATE", state: { kind: "idle" } });
    } else if (result.outcome === "failed") {
      dispatch({
        type: "SET_REPORT_STATE",
        state: { kind: "error", code: result.code, message: result.message },
      });
      dispatch({
        type: "SET_RESEARCH_STATE",
        state: { kind: "error", code: result.code, message: result.message },
      });
    } else {
      dispatch({
        type: "SET_REPORT_STATE",
        state: { kind: "success", report: result.report },
      });
      dispatch({ type: "SET_RESEARCH", research: result.research });
      dispatch({
        type: "SET_RESEARCH_STATE",
        state: {
          kind: "success",
          research: result.research,
          providerMetadata: {
            providerName: "openai",
            modelUsed: "gpt-research-report",
          },
          warnings: [],
        },
      });
    }
  }, [
    state.dna,
    state.extraction,
    state.initialFile,
    state.detection,
    state.periodOverride,
    state.selectedCompany,
  ]);

  // The comprehensive report IS the single research step now. These names are
  // kept so existing callers (research button, retries) route to it.
  const runResearch = generateFullResearchReport;
  const retryFailedResearchPasses = generateFullResearchReport;
  const retryAllResearch = generateFullResearchReport;

  const runOrchestratedGeneration = useCallback(
    async (
      withResearch: boolean,
      mode: "fresh" | "resume",
    ): Promise<void> => {
      if (!state.dna || !state.extraction || !state.initialFile) return;
      const companyName =
        state.selectedCompany?.companyName ??
        state.periodOverride.detectedCompany ??
        state.detection?.detectedCompany ??
        state.initialFile.filename.replace(/\.[^.]+$/, "");
      const ticker =
        state.selectedCompany?.ticker ??
        state.detection?.detectedTicker ??
        companyName;
      const periodLabel =
        state.periodOverride.periodLabel ??
        (state.detection?.best ? renderPeriodLabel(state.detection.best) : "");
      const research = withResearch ? state.research : null;

      const failedId =
        mode === "resume" ? state.progress.failedSectionId : undefined;
      const resumeFromIdx =
        mode === "resume" && failedId
          ? Math.max(0, CANONICAL_SECTION_IDS.indexOf(failedId))
          : 0;
      const existingSections =
        mode === "resume" ? state.completedSections : {};

      // Coverage-driven skip list — the user's principle: the follow-up
      // memo should only contain what the original memo actually covered.
      // Sections without a clear analog in the source memo are dropped.
      // Computed BEFORE START_GENERATION so the progress UI never seeds a
      // permanently-pending row for a section we won't draft.
      const coverage = state.memoCoverage;
      const exclude: CanonicalSectionId[] = [];
      // ALWAYS exclude the "Updated Investment View" section. The analyst
      // does not want an AI-generated investment recommendation in any
      // part of the output — the memo presents facts; the human draws the
      // conclusion. (This was sec_investment_action.)
      exclude.push("sec_investment_action");
      if (coverage && !coverage.shareholding) exclude.push("sec_shareholding");
      // The supplementary panels carry the deepest valuation math — keep
      // valuation_detail always on (it's the most-asked-for content), but
      // skip the EPS bridge / forecasts-vs-actuals panels when the memo had
      // no forecasts or valuation framework to anchor them to.
      if (coverage && !coverage.forecasts) exclude.push("sup_eps_bridge");
      if (coverage && !coverage.forecasts) exclude.push("sup_financials_actuals");

      generateAbort.current?.abort();
      const controller = new AbortController();
      generateAbort.current = controller;
      dispatch({
        type: "START_GENERATION",
        startedAt: new Date().toISOString(),
        resumeFromIdx,
        excludeSectionIds: exclude,
      });
      dispatch({ type: "SET_LLM_STATE", state: { kind: "loading" } });

      // Server-fetch the day's live price. If the research orchestrator
      // already injected one into baseDetection, this would normally be
      // redundant — but section generation can be invoked independently
      // (retry, run without prior research), so we always re-fetch. Best
      // effort: null when unavailable, in which case the LLM falls back to
      // its own search rules.
      const livePriceForGen = await fetchCurrentPrice(
        state.selectedCompany,
        controller.signal,
      );
      if (controller.signal.aborted) return;

      const understandingForGen =
        state.understanding.kind === "success"
          ? state.understanding.understanding
          : null;
      const memoUnderstandingDigestForGen = understandingForGen
        ? buildMemoUnderstandingDigest(understandingForGen)
        : undefined;
      // User priorities deliberately NOT threaded into section gen anymore;
      // the dashboard-only PrioritiesAnswer pipeline handles them after
      // the memo completes.

      const result = await runSectionGeneration({
        project: {
          id: state.dna.projectId,
          ticker,
          companyName,
          sector: state.selectedCompany?.sector,
        },
        dna: state.dna,
        excludeSectionIds: exclude,
        detection: periodLabel
          ? {
              detectedCompany: companyName,
              periodLabel,
              researchStart: state.periodOverride.researchStart,
              researchCurrent:
                state.detection?.researchCurrent ??
                new Date().toISOString().slice(0, 7),
              assumptionNotes: state.detection?.assumptionNotes ?? [],
              memoWrittenOn: state.detection?.memoWrittenOn,
              currentPrice: livePriceForGen ?? undefined,
            }
          : undefined,
        research,
        initialMemoId: state.initialFile.id,
        memoUnderstandingDigest: memoUnderstandingDigestForGen,
        apiCall: (req, signal) => api.generateMemoSection(req, signal),
        signal: controller.signal,
        onSectionStart: (sectionId, attempt) => {
          dispatch({ type: "SECTION_STARTED", sectionId, attempt });
        },
        onSectionDone: (sectionId, section) => {
          dispatch({ type: "SECTION_SUCCESS", sectionId, section });
        },
        onSectionFail: () => {
          // SECTION_FAILED dispatch happens after the result resolves so we
          // can also set the LLM error state in lockstep.
        },
        startFromSectionId: failedId,
        existingSections,
      });

      if (generateAbort.current !== controller) return;
      generateAbort.current = null;

      if (result.ok) {
        dispatch({ type: "SET_GENERATED_MEMO", memo: result.memo });
        dispatch({
          type: "SET_LLM_STATE",
          state: {
            kind: "success",
            memo: result.memo,
            providerMetadata: {
              providerName: "openai",
              modelUsed: "gpt-section",
            },
            usedFallback: false,
            warnings: [],
          },
        });
        // Fire the dashboard-only Priorities Q&A pipeline now that the memo
        // is done. Best-effort: a failure here doesn't roll back the memo;
        // the dashboard simply doesn't render the priorities card.
        const priorities = state.userResearchPriorities.trim();
        if (priorities.length > 0) {
          dispatch({ type: "SET_PRIORITIES_ANSWER", state: { kind: "loading" } });
          void api
            .generatePrioritiesAnswer(
              {
                project: {
                  id: state.dna.projectId,
                  ticker,
                  companyName,
                  sector: state.selectedCompany?.sector,
                },
                dna: state.dna,
                detection: periodLabel
                  ? {
                      detectedCompany: companyName,
                      periodLabel,
                      researchStart: state.periodOverride.researchStart,
                      researchCurrent:
                        state.detection?.researchCurrent ??
                        new Date().toISOString().slice(0, 7),
                      assumptionNotes: state.detection?.assumptionNotes ?? [],
                      memoWrittenOn: state.detection?.memoWrittenOn,
                      currentPrice: livePriceForGen ?? undefined,
                    }
                  : undefined,
                research,
                memoUnderstandingDigest: memoUnderstandingDigestForGen,
                userPriorities: priorities,
                initialMemoId: state.initialFile.id,
              },
              controller.signal,
            )
            .then((res) => {
              if (controller.signal.aborted) return;
              if (res.ok) {
                dispatch({
                  type: "SET_PRIORITIES_ANSWER",
                  state: { kind: "success", answer: res.answer },
                });
              } else {
                dispatch({
                  type: "SET_PRIORITIES_ANSWER",
                  state: { kind: "error", code: res.code, message: res.message },
                });
              }
            })
            .catch((err: unknown) => {
              if (controller.signal.aborted) return;
              dispatch({
                type: "SET_PRIORITIES_ANSWER",
                state: {
                  kind: "error",
                  code: "provider_error",
                  message: err instanceof Error ? err.message : "Network error",
                },
              });
            });
        }
        return;
      }

      if (result.code === "aborted") {
        return;
      }

      // Phase 6H: a stale_client signal in the failure means this bundle
      // POSTed an id the deployed worker no longer accepts — flip the
      // reload banner instead of leaving a cryptic provider_error.
      if (result.message.includes("stale_client")) {
        dispatch({ type: "SET_STALE_CLIENT", value: true });
      }

      if (result.failedSectionId) {
        dispatch({
          type: "SECTION_FAILED",
          sectionId: result.failedSectionId,
          code: result.code,
          message: result.message,
        });
      }
      dispatch({
        type: "SET_LLM_STATE",
        state: {
          kind: "error",
          error: `${result.code} · ${result.message}`,
        },
      });
    },
    [
      state.dna,
      state.extraction,
      state.initialFile,
      state.research,
      state.detection,
      state.periodOverride,
      state.progress.failedSectionId,
      state.completedSections,
      state.understanding,
      state.userResearchPriorities,
      state.selectedCompany,
      state.memoCoverage,
    ],
  );

  const generateMemo = useCallback(
    (withResearch: boolean): Promise<void> => {
      dispatch({ type: "RESET_PROGRESS" });
      return runOrchestratedGeneration(withResearch, "fresh");
    },
    [runOrchestratedGeneration],
  );

  const retryFailedSection = useCallback(
    (): Promise<void> =>
      runOrchestratedGeneration(Boolean(state.research), "resume"),
    [runOrchestratedGeneration, state.research],
  );

  const retryFullMemo = useCallback((): Promise<void> => {
    dispatch({ type: "RESET_PROGRESS" });
    return runOrchestratedGeneration(Boolean(state.research), "fresh");
  }, [runOrchestratedGeneration, state.research]);

  const startOver = useCallback(() => {
    researchAbort.current?.abort();
    generateAbort.current?.abort();
    reportAbort.current?.abort();
    researchAbort.current = null;
    generateAbort.current = null;
    reportAbort.current = null;
    dispatch({ type: "RESET" });
  }, []);

  const value = useMemo<MemoProjectContextValue>(() => {
    const effectiveDetection = state.detection
      ? {
          detectedCompany:
            state.periodOverride.detectedCompany ??
            state.detection.detectedCompany ??
            "",
          periodLabel:
            state.periodOverride.periodLabel ??
            (state.detection.best
              ? renderPeriodLabel(state.detection.best)
              : ""),
          researchStart: state.periodOverride.researchStart,
          researchCurrent: state.detection.researchCurrent,
          assumptionNotes: state.detection.assumptionNotes,
        }
      : null;

    return {
      state,
      effectiveDetection,
      setSelectedCompany,
      clearSelectedCompany,
      extractInitialMemo,
      setPeriodOverride,
      runResearch,
      retryFailedResearchPasses,
      retryAllResearch,
      generateFullResearchReport,
      generateMemo,
      retryFailedSection,
      retryFullMemo,
      refreshLlmProviderStatus,
      syncGateTokenSet,
      startOver,
      runMemoUnderstanding,
      rerunMemoUnderstanding,
      skipMemoUnderstanding,
      setUserResearchPriorities,
    };
  }, [
    state,
    setSelectedCompany,
    clearSelectedCompany,
    extractInitialMemo,
    setPeriodOverride,
    runResearch,
    retryFailedResearchPasses,
    retryAllResearch,
    generateFullResearchReport,
    generateMemo,
    retryFailedSection,
    retryFullMemo,
    refreshLlmProviderStatus,
    syncGateTokenSet,
    startOver,
    runMemoUnderstanding,
    rerunMemoUnderstanding,
    skipMemoUnderstanding,
    setUserResearchPriorities,
  ]);

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useMemoProject(): MemoProjectContextValue {
  const ctx = useContext(Ctx);
  if (!ctx) {
    throw new Error("useMemoProject must be used inside <MemoProjectProvider>");
  }
  return ctx;
}

function renderPeriodLabel(p: {
  kind: string;
  isoDate?: string;
  isoMonth?: string;
  monthLabel?: string;
  quarter?: string;
  fiscalYearLabel?: string;
  rawMatch: string;
}): string {
  switch (p.kind) {
    case "iso_date":
      return p.isoDate ?? p.rawMatch;
    case "month_year":
      return p.monthLabel ?? p.isoMonth ?? p.rawMatch;
    case "quarter_fy":
      return `${p.quarter ?? ""} ${p.fiscalYearLabel ?? ""}`.trim();
    case "fiscal_year":
      return p.fiscalYearLabel ?? p.rawMatch;
    default:
      return p.rawMatch;
  }
}
